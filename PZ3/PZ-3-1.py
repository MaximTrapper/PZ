# Ввод координат точки
x = float(input("Введите координату x: "))
y = float(input("Введите координату y: "))

# Проверка, лежит ли точка в четвертой четверти
is_in_fourth_quadrant = x > 0 and y < 0

# Вывод результата
print(f"Точка ({x}, {y}) лежит в четвертой координатной четверти: {is_in_fourth_quadrant}")