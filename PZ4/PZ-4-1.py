# Ввод чисел A и B
A = int(input("Введите число A: "))
B = int(input("Введите число B: "))

# Инициализация счетчика
count = 0

# Вывод чисел в порядке убывания
print(f"Числа между {A} и {B} в порядке убывания:")
for number in range(B - 1, A, -1):
    print(number, end=" ")
    count += 1

# Вывод количества чисел
print(f"\nКоличество чисел: {count}")